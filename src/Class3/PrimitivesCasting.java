package Class3;

public class PrimitivesCasting {

	public static void main(String[] args) {
		
		// Widening it means automatic increasing 
		//EXP 1
		
		int x=123;
		double y= 56;
		
		System.out.println(x);
		System.out.println(y);// 56 change atomic to 56.0
		System.out.println("-------------");
		
		//  Narrowing it means manual we should change 
		// EXP 2
		int f=78;
		double z= 89.99;
		int h=(int)z;
		System.out.println(f);
		System.out.println(h);// 89.99 change to 89 
		System.out.println("------------------");
		
		
		
		
		
		
	}
	

		 
	
	
	
		
		
		
		
		
		
		
		
		
		
		
		
	}


