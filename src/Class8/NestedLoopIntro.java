package Class8;

public class NestedLoopIntro {

	public static void main(String[] args) {
		
		for(int i=1; i<=3; i++) {
			System.out.println("Hello");
			for(int y=1; y<=2; y++) {
				System.out.println("Bye");
			}
			
		}
		System.out.println();
		
		for(int i=1;i<=3; i++) {
			System.out.print(i+" ");
			for(int x=1; x<=2; x++) {
				System.out.print(x);
			}
		}
		
		

	
		
		
		
	}

}
